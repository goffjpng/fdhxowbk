		<p>Yes, mussels of certain waters in Scotland, Wales, Ireland, Saxony, Bohemia, and France. We were furrowing the waters of the Indian Ocean, a vast liquid plain with a surface of 1,200,000,000 acres, and whose waters are so clear and transparent that anyone leaning over them would turn giddy. The Nautilus usually floated between fifty and a hundred fathoms deep. We went on so for some days. To anyone but myself, who had a great love for the sea, the hours would have seemed long and monotonous; but the daily walks on the platform, when I steeped myself in the reviving air of the ocean, the sight of the rich waters through the windows of the saloon, the books in the library, the compiling of my memoirs, took up all my time, and left me not a moment of ennui or weariness. In my own, sir! Really, I do not know how to answer, master. We are sure to see curious things, and for the last two months we have not had time for ennui. The last marvel is always the most astonishing; and if we continue this progression, I do not know how it will end. It is my opinion that we shall never again see the like. I think, then, with no offense to master, that a happy year would be one in which we could see everything.</p>
